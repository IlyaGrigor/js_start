// Напишите функцию calculateAverage, которая принимает 2 числа и возвращает их среднее арифметическое

function calculateAverage(numb_1, numb_2) {
    let average = (numb_1 + numb_2)/2;
    return +average;
}

let averageMain_1 = calculateAverage(2, 3);
console.log(averageMain_1);
let averageMain_2 = calculateAverage(3, 4);
console.log(averageMain_2);
let averageMain_3 = calculateAverage(7, 6);
